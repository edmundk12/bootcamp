public class Cat {
    // attributes
    private String color;
    private String name;
    private int age;

    // instance method (object method)
    // getter
  public String getName() {
    return this.name;
  }
  public void setName(String name) {
    this.name = name;
  }

    public String getColor() {
      return this.color;
    }
    public void setColor(String color) {
      this.color = color;
    }

    public int getAge() {
      return this.age;
    }
    public void setAge(int age) {
      this.age = age;
    }
  }
